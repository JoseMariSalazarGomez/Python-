pregunta1 = int(input("Introduce un numero: "))


if pregunta1 >= 0:
    print("Tu numero es positivo")
else:
    print("Tu numero es negativo")

if pregunta1 % 2 == 0:
    print("Tu numero es par")
else:
    print("Tu numero es impar")

if pregunta1 > 10:
    print("Tu numero es mayor a 10")
else:
    print("Tu numero es menor que 10")



